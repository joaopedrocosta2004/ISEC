    private void jMenuItem19ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jMenuItem19ActionPerformed
        try {
            SAXBuilder saxBuilder = new SAXBuilder();

            // Ler factos.xml
            Document factosDocument = saxBuilder.build(new File("factos.xml"));
            List<Element> factosList = factosDocument.getRootElement().getChildren("facto");

            // Ler countries.xml
            Document countriesDocument = saxBuilder.build(new File("countries.xml"));
            List<Element> countriesList = countriesDocument.getRootElement().getChildren("countrie");

            // Mapear países de countries.xml por nome
            Map<String, Element> countriesMap = new HashMap<>();
            for (Element countrie : countriesList) {
                String nome = countrie.getAttributeValue("nome");
                countriesMap.put(nome, countrie);
            }

            // Criar o documento combinado
            Element rootElement = new Element("paises_combinados");
            Document combinedDocument = new Document(rootElement);

            // Combinar informações de factos.xml e countries.xml
            for (Element facto : factosList) {
                String nome = facto.getAttributeValue("nome");

                Element combinedElement = new Element("pais");
                combinedElement.setAttribute("nome", nome);

                combinedElement.addContent(new Element("iso").setText(facto.getChildText("iso")));
                combinedElement.addContent(new Element("continente").setText(facto.getChildText("continente")));
                combinedElement.addContent(new Element("presidente_republica").setText(facto.getChildText("presidente_republica")));
                combinedElement.addContent(new Element("imagem").setText(facto.getChildText("imagem")));
                combinedElement.addContent(new Element("capital").setText(facto.getChildText("capital")));
                combinedElement.addContent(new Element("moeda").setText(facto.getChildText("moeda")));
                combinedElement.addContent(new Element("populacao").setText(facto.getChildText("populacao")));
                combinedElement.addContent(new Element("crescimento_populacao").setText(facto.getChildText("crescimento_populacao")));
                combinedElement.addContent(new Element("area").setText(facto.getChildText("area")));

                Element idiomasElement = new Element("idiomas_oficiais");
                List<Element> idiomasList = facto.getChild("idiomas_oficiais").getChildren("idioma");
                for (Element idioma : idiomasList) {
                    idiomasElement.addContent(new Element("idioma").setText(idioma.getText()));
                }
                combinedElement.addContent(idiomasElement);

                Element cidadesPopulosasElement = new Element("cidades_populosas");
                if (facto.getChild("cidades_populosas") != null) {
                    List<Element> cidadesList = facto.getChild("cidades_populosas").getChildren("cidade");
                    for (Element cidade : cidadesList) {
                        cidadesPopulosasElement.addContent(new Element("cidade").setText(cidade.getText()));
                    }
                }
                combinedElement.addContent(cidadesPopulosasElement);

                Element paisesVizinhosElement = new Element("paises_vizinhos");
                if (facto.getChild("paises_vizinhos") != null) {
                    List<Element> paisesList = facto.getChild("paises_vizinhos").getChildren("pais");
                    for (Element pais : paisesList) {
                        paisesVizinhosElement.addContent(new Element("pais").setText(pais.getText()));
                    }
                }
                combinedElement.addContent(paisesVizinhosElement);

                combinedElement.addContent(new Element("dominio_internet").setText(facto.getChildText("dominio_internet")));

                // Adicionar informações adicionais de countries.xml, se existirem
                Element countryElement = countriesMap.get(nome);
                if (countryElement != null) {
                    // Adicionar ou sobrescrever elementos específicos se necessário
                    combinedElement.getChild("iso").setText(countryElement.getChildText("iso"));
                    combinedElement.getChild("continente").setText(countryElement.getChildText("continente"));
                    combinedElement.getChild("presidente_republica").setText(countryElement.getChildText("presidente_republica"));
                    combinedElement.getChild("imagem").setText(countryElement.getChildText("imagem"));
                }

                rootElement.addContent(combinedElement);
            }

            // Escrever o resultado em um novo arquivo XML
            XMLOutputter xmlOutput = new XMLOutputter();
            xmlOutput.setFormat(Format.getPrettyFormat());
            xmlOutput.output(combinedDocument, new FileWriter("paises_combinados.xml"));

            String successMessage = "Arquivo 'paises_combinados.xml' gerado com sucesso.";
            jTextArea1.setText(successMessage);
            System.out.println(successMessage);

        } catch (IOException ex) {
            Logger.getLogger(Frame.class.getName()).log(Level.SEVERE, null, ex);
        } catch (JDOMException ex) {
            Logger.getLogger(Frame.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jMenuItem19ActionPerformed


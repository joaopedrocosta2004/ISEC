        // Compilação da consulta XQuery
        String query = String.format(
            "for $facto in //facto[number(area) > %s] " +
            "return <pais>{$facto/@nome}</pais>", areaValue
        );
        XQueryExecutable exec = compiler.compile(query);

        // Avaliação da consulta XQuery
        XQueryEvaluator eval = exec.load();

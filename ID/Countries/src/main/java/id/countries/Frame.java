    private void jButton16ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton16ActionPerformed
        // TODO add your handling code here:
        String lingua = jTextField27.getText().trim();
    
    if (lingua != null && !lingua.isEmpty()) {
        // Caminho absoluto para o arquivo XML de entrada
        String xmlFilePath = "C:/Users/joaos/OneDrive/Documentos/NetBeansProjects/Countries/factos.xml";
        // Caminho absoluto para o arquivo de saída TXT
        String outputPath = "C:/Users/joaos/OneDrive/Documentos/NetBeansProjects/Countries/paisesComLinguaEmComum.txt";

        File xmlFile = new File(xmlFilePath);
        if (!xmlFile.exists()) {
            JOptionPane.showMessageDialog(this, "O arquivo XML não foi encontrado: " + xmlFilePath, "Erro", JOptionPane.ERROR_MESSAGE);
            return;
        }

        try {
            // Configurações do Saxon
            Processor proc = new Processor(false);
            DocumentBuilder builder = proc.newDocumentBuilder();
            XdmNode inputDoc = builder.build(new StreamSource(xmlFilePath));
            
            // Consulta XQuery
            String query = String.format(
                "for $facto in //facto[idiomas_oficiais/idioma = '%s'] " +
                "return $facto/@nome", lingua
            );

            // Compilação e execução da consulta
            XQueryCompiler compiler = proc.newXQueryCompiler();
            XQueryExecutable exec = compiler.compile(query);
            XQueryEvaluator eval = exec.load();
            eval.setContextItem(inputDoc);
            XdmValue result = eval.evaluate();

            // Geração do ficheiro TXT
            try (BufferedWriter writer = new BufferedWriter(new FileWriter(outputPath))) {
                for (XdmItem item : result) {
                    writer.write(item.getStringValue());
                    writer.newLine();
                }
            }

            JOptionPane.showMessageDialog(this, "Ficheiro gerado com sucesso em " + outputPath, "Sucesso", JOptionPane.INFORMATION_MESSAGE);
        } catch (SaxonApiException | IOException e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Ocorreu um erro: " + e.getMessage(), "Erro", JOptionPane.ERROR_MESSAGE);
        }
    } else {
        JOptionPane.showMessageDialog(this, "Língua não fornecida ou inválida.", "Aviso", JOptionPane.WARNING_MESSAGE);
    }
    }//GEN-LAST:event_jButton16ActionPerformed


    private void jMenuItem8ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jMenuItem8ActionPerformed
        try {
            // TODO add your handling code here:

            // Carregar os documentos XML
            SAXBuilder saxBuilder = new SAXBuilder();
            Document documentFactos = saxBuilder.build(new File("factos.xml"));
            Document documentCountries = saxBuilder.build(new File("countries.xml"));

            // Buscar todos os elementos "facto" no factos.xml
            List<Element> paisesFactos = documentFactos.getRootElement().getChildren("facto");

            // Buscar todos os elementos "countrie" no countries.xml
            List<Element> paisesCountries = documentCountries.getRootElement().getChildren("countrie");

            // Criar um Set para armazenar os nomes dos países (para evitar duplicados)
            Set<String> nomesPaises = new HashSet<>();

            // Adicionar cada nome de país de factos.xml ao Set
            for (Element pais : paisesFactos) {
                String nomePais = pais.getAttributeValue("nome");
                if (nomePais != null && !nomePais.trim().isEmpty()) {
                    nomesPaises.add(nomePais.trim());
                }
            }

            // Adicionar cada nome de país de countries.xml ao Set
            for (Element pais : paisesCountries) {
                String nomePais = pais.getAttributeValue("nome");
                if (nomePais != null && !nomePais.trim().isEmpty()) {
                    nomesPaises.add(nomePais.trim());
                }
            }

            // Criar StringBuilder para armazenar os nomes dos países
            StringBuilder resultado = new StringBuilder();

            // Adicionar cada nome de país do Set ao StringBuilder
            for (String nomePais : nomesPaises) {
                resultado.append(nomePais).append("\n");
            }

            // Escrever o resultado em um arquivo TXT
            try (FileWriter writer = new FileWriter("listagem_paises.txt")) {
                writer.write(resultado.toString());
            }

            String successMessage = "Arquivo 'listagem_paises.txt' gerado com sucesso.";
            jTextArea1.setText(successMessage);
            System.out.println(successMessage);

        } catch (JDOMException ex) {
            Logger.getLogger(Frame.class.getName()).log(Level.SEVERE, null, ex);
        } catch (IOException ex) {
            Logger.getLogger(Frame.class.getName()).log(Level.SEVERE, null, ex);
        }

    }//GEN-LAST:event_jMenuItem8ActionPerformed

    private void jMenuItem9ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jMenuItem9ActionPerformed
        try {
            // TODO add your handling code here:

            // Carregar o documento XML
            SAXBuilder saxBuilder = new SAXBuilder();
            Document document = saxBuilder.build(new File("factos.xml"));

            // Buscar todos os elementos "facto"
            List<Element> paises = document.getRootElement().getChildren("facto");

            // Ordenar os países pela população em ordem decrescente e pegar os 5 primeiros
            List<Element> top5Paises = paises.stream()
                    .sorted((pais1, pais2) -> {
                        long pop1 = Long.parseLong(pais1.getChildText("populacao").replace(",", ""));
                        long pop2 = Long.parseLong(pais2.getChildText("populacao").replace(",", ""));
                        return Long.compare(pop2, pop1);
                    })
                    .limit(5)
                    .collect(Collectors.toList());

            // Criar um novo documento XML para armazenar os 5 países mais populosos
            Element rootElement = new Element("top5PaisesPopulosos");
            Document top5Document = new Document(rootElement);

            for (Element pais : top5Paises) {
                Element paisClone = pais.clone();
                rootElement.addContent(paisClone);
            }

            // Escrever o novo documento XML em um arquivo
            try (FileWriter writer = new FileWriter("top5_paises_populosos.xml")) {
                XMLOutputter xmlOutputter = new XMLOutputter(Format.getPrettyFormat());
                xmlOutputter.output(top5Document, writer);
            }

            String successMessage = "Arquivo 'top5_paises_populosos.xml' gerado com sucesso.";
            jTextArea1.setText(successMessage);
            System.out.println(successMessage);

        } catch (JDOMException ex) {
            Logger.getLogger(Frame.class.getName()).log(Level.SEVERE, null, ex);
        } catch (IOException ex) {
            Logger.getLogger(Frame.class.getName()).log(Level.SEVERE, null, ex);
        }

    }//GEN-LAST:event_jMenuItem9ActionPerformed


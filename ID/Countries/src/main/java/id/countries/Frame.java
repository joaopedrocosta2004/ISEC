    private void jButton17ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton17ActionPerformed
        // TODO add your handling code here:
        String pesquisa = jTextField28.getText();
        String pesquisaPopulacaoStr = jTextField29.getText();
        long pesquisaPopulacao = Long.parseLong(pesquisaPopulacaoStr);

        jDialog17.setVisible(false);
        try {
            // Carregar o documento XML
            SAXBuilder saxBuilder = new SAXBuilder();
            Document document = saxBuilder.build(new File("factos.xml"));

            // Buscar todos os países do continente especificado
            List<Element> paises = document.getRootElement().getChildren("facto");
            paises.removeIf(pais -> !pesquisa.equals(pais.getChildText("continente")));

            // Verificar se há países para o continente especificado
            if (paises.isEmpty()) {
                jTextArea1.setText("O continente " + pesquisa + " não existe.");
                return;
            }

            // Construir uma lista de países com a mesma população especificada
            StringBuilder resultado = new StringBuilder();
            boolean encontrouPaises = false;
            for (Element pais : paises) {
                String populacaoStr = pais.getChildText("populacao");
                if (populacaoStr != null && !populacaoStr.isEmpty()) {
                    long populacao = Long.parseLong(populacaoStr.replace(",", ""));
                    if (populacao == pesquisaPopulacao) {
                        resultado.append(pais.getAttributeValue("nome")).append("\n");
                        encontrouPaises = true;
                    }
                }
            }

            if (encontrouPaises) {
                jTextArea1.setText("Países no continente " + pesquisa + " com população " + pesquisaPopulacao + ":\n" + resultado.toString());
            } else {
                jTextArea1.setText("Nenhum país no continente " + pesquisa + " tem a população " + pesquisaPopulacao + ".");
            }
        } catch (Exception e) {
            Logger.getLogger(Frame.class.getName()).log(Level.SEVERE, null, e);
        }
    }//GEN-LAST:event_jButton17ActionPerformed

